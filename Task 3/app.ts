// Name Cases :

let myName :string = "Sabiha";

// Upper case :
console.log("Upper Case :", myName.toUpperCase());

// Lower case :
console.log("lower Case :", myName.toLowerCase());

// Title case :
let my_name :string = "sabiha";
console.log("Title Case :",my_name.charAt(0).toUpperCase() + my_name.slice(1).toLowerCase());
